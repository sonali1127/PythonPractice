a=int(input("Enter the 1st number:"))
b=int(input("Enter the 2nd number:"))
c=input("Enter the symbol such as(+,-,*,/,//,%,^):")
if c=="+":
    print(a+b)
elif c=="-":
    print(a-b)
elif c=="*":
    print(a*b)
elif c=="/":
    print(a/b)
elif c=="%":
    print(a%b)
elif c=="//":
    print(a//b)
elif c=="^":
    print(a**b)